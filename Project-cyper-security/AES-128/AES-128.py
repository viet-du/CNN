RCON = (
    0x00, 0x01, 0x02, 0x04, 0x08, 0x10, 0x20, 0x40, 0x80, 0x1B, 0x36
)

def galois_multiply(a, b):
    """Phép nhân trong trường Galois GF(2^8)"""
    p = 0
    for _ in range(8):
        if b & 1:
            p ^= a
        hi_bit_set = a & 0x80
        a <<= 1
        if hi_bit_set:
            a ^= 0x1B # Đa thức x^8 + x^4 + x^3 + x + 1
        b >>= 1
    return p % 256

def sub_bytes(state, sbox):
    for i in range(16):
        state[i] = sbox[state[i]]

def shift_rows(state, inv=False):
    """
    state là mảng 1 cột (16 bytes).
    Theo thứ tự cột, tức là state[0..3] là cột 0, state[4..7] là cột 1...
    Matrix:
    0  4  8  12
    1  5  9  13
    2  6  10 14
    3  7  11 15
    """
    new_state = state.copy()
    if not inv:
        # Row 1: left shift 1
        new_state[1], new_state[5], new_state[9], new_state[13] = state[5], state[9], state[13], state[1]
        # Row 2: left shift 2
        new_state[2], new_state[6], new_state[10], new_state[14] = state[10], state[14], state[2], state[6]
        # Row 3: left shift 3
        new_state[3], new_state[7], new_state[11], new_state[15] = state[15], state[3], state[7], state[11]
    else:
        # Row 1: right shift 1
        new_state[1], new_state[5], new_state[9], new_state[13] = state[13], state[1], state[5], state[9]
        # Row 2: right shift 2
        new_state[2], new_state[6], new_state[10], new_state[14] = state[10], state[14], state[2], state[6]
        # Row 3: right shift 3
        new_state[3], new_state[7], new_state[11], new_state[15] = state[7], state[11], state[15], state[3]
    return new_state

def mix_columns(state, inv=False):
    new_state = [0] * 16
    # Ma trận nhân cho MixColumns (Mã hóa) và InvMixColumns (Giải mã)
    m = [0x0e, 0x0b, 0x0d, 0x09] if inv else [2, 3, 1, 1]
    
    for i in range(4):
        c = state[i*4 : (i+1)*4] # Lấy từng cột 4 bytes
        for j in range(4):
            # Nhân ma trận vòng bằng (x - j) % 4 để xoay giá trị ma trận m
            new_state[i*4 + j] = (galois_multiply(c[0], m[(0-j)%4]) ^
                                  galois_multiply(c[1], m[(1-j)%4]) ^
                                  galois_multiply(c[2], m[(2-j)%4]) ^
                                  galois_multiply(c[3], m[(3-j)%4]))
    return new_state

def add_round_key(state, key):
    for i in range(16):
        state[i] ^= key[i]

def expand_key(key):
    """Mở rộng khóa 128-bit (16 bytes) thành mảng khóa 176 bytes"""
    expanded = list(key)
    bytes_generated = 16
    rcon_iteration = 1

    while bytes_generated < 176:
        core = expanded[-4:]
        
        # Cứ sau 16 bytes thì áp dụng Key Schedule Core
        if bytes_generated % 16 == 0:
            # Dịch vòng trái (Rotate left)
            core = core[1:] + core[:1]
            # SubBytes
            core = [SBOX[b] for b in core]
            # XOR với RCON
            core[0] ^= RCON[rcon_iteration]
            rcon_iteration += 1

        # XOR với word cách đó 16 bytes
        for i in range(4):
            expanded.append(expanded[bytes_generated - 16] ^ core[i])
            bytes_generated += 1

    # Chuyển đổi thành 11 mảng con, mỗi mảng 16 bytes
    return [expanded[i*16:(i+1)*16] for i in range(11)]

def pkcs7_pad(data):
    """Padding dữ liệu để độ dài chia hết cho 16"""
    padding_len = 16 - (len(data) % 16)
    return data + bytes([padding_len] * padding_len)

def pkcs7_unpad(data):
    """Bỏ padding"""
    padding_len = data[-1]
    if padding_len < 1 or padding_len > 16:
        raise ValueError("Padding không hợp lệ")
    return data[:-padding_len]

def aes_encrypt_block(plaintext, round_keys):
    """Mã hóa 1 khối 16 bytes"""
    state = list(plaintext)
    
    # Initial Round
    add_round_key(state, round_keys[0])

    # 9 Rounds
    for i in range(1, 10):
        sub_bytes(state, SBOX)
        state = shift_rows(state)
        state = mix_columns(state)
        add_round_key(state, round_keys[i])

    # Final Round (No MixColumns)
    sub_bytes(state, SBOX)
    state = shift_rows(state)
    add_round_key(state, round_keys[10])

    return bytes(state)

def aes_decrypt_block(ciphertext, round_keys):
    """Giải mã 1 khối 16 bytes"""
    state = list(ciphertext)

    # Initial Round
    add_round_key(state, round_keys[10])
    state = shift_rows(state, inv=True)
    sub_bytes(state, INV_SBOX)

    # 9 Rounds
    for i in range(9, 0, -1):
        add_round_key(state, round_keys[i])
        state = mix_columns(state, inv=True)
        state = shift_rows(state, inv=True)
        sub_bytes(state, INV_SBOX)

    # Final Round (No MixColumns)
    add_round_key(state, round_keys[0])

    return bytes(state)

def aes128_encrypt(plaintext: bytes, key: bytes) -> bytes:
    if len(key) != 16:
        raise ValueError("Khóa phải có độ dài 16 bytes (128 bits).")
        
    round_keys = expand_key(key)
    padded_data = pkcs7_pad(plaintext)
    
    ciphertext = b""
    for i in range(0, len(padded_data), 16):
        block = padded_data[i:i+16]
        ciphertext += aes_encrypt_block(block, round_keys)
        
    return ciphertext

def aes128_decrypt(ciphertext: bytes, key: bytes) -> bytes:
    if len(key) != 16:
        raise ValueError("Khóa phải có độ dài 16 bytes (128 bits).")
        
    round_keys = expand_key(key)
    
    plaintext_padded = b""
    for i in range(0, len(ciphertext), 16):
        block = ciphertext[i:i+16]
        plaintext_padded += aes_decrypt_block(block, round_keys)
        
    return pkcs7_unpad(plaintext_padded)

